package test2;

public class Node {
	private int key;
	private int val;
	public Node(int key, int val)
	{
		this.key = key;
		this.val = val;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	
}
